public class forlloop1 { 

	public static void main(String[] args) {
	
			
				for(int i=1; i<=1; i++)
					{
					 {
					System.out.println("1");
				
					for(int j=1; j<=1; j++) {
						System.out.println("11");
					}
						for(int k=1; k<=1; k++);
						System.out.println("111");
					 }	
						for(int l=1; l<=1; l++);
						System.out.println("1111");
					}
						for(int m=1; m<=1; m++);
						System.out.println("11111");
					 				
					 }
	}
					



